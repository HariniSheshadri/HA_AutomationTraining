package com.healthasyst.methods;

public class Area {
	//area of circle
	
		public static double areaOfCircle(int r) {
			double areaC=3.14*r*r;
			return areaC;
		}
		
		//areaof triangle
		
		public static double areaOfTriangle(double b, double h) {
			double areaT=(b*h)/2;
			return areaT;
		}
		

}
